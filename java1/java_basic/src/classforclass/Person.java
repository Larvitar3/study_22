package classforclass;

public class Person {

	private String name;
	private int age;
	
	public String Person() {
		return name;
	};
	
	public Person(String name) {
		this.name = name;
	};
	
	

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getAge() {
		return age;
	}

	public void setAge(int age) {
		this.age = age;
	};
	
	
	
	
	
}
